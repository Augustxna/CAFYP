package com.example.cafyp.domain;

public class ChatlistData {
    String phoneno, fullName, resadminid, memberusename;

    public String getPhoneno() {
        return phoneno;
    }

    public void setPhoneno(String phoneno) {
        this.phoneno = phoneno;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getResadminid() {
        return resadminid;
    }

    public void setResadminid(String resadminid) {
        this.resadminid = resadminid;
    }

    public String getMemberusename() {
        return memberusename;
    }

    public void setMemberusename(String memberusename) {
        this.memberusename = memberusename;
    }
}
